// In this class we discuss java While loop
public class Tut08 {
    public static void main(String[] args) {

        int i = 1;
        while(i<=5){
            System.out.println("Hello");
            i++;
        }

        int x = 10;
        while(x<=20){
            System.out.println(x);
            x= x+2;
        }


    }
}
