// In this class we discuss java Do While loop
public class Tut09 {
    public static void main(String[] args) {

        int i = 1;

        do {
            System.out.println("JAVA");
            i++;
        }
        while(i<=10);

//        The Difference between while loop and do while loop is:
//        while loop checks the condition and only does the task if the condition is true
//        But the do while loop will always will do the task once and then checks if the condition is true

        int a = 17;

        do {
            System.out.println("JAVA");
            a++;
        }
        while(a<=10);


    }
}
