// In this class we discuss java for loop
public class Tut11 {
    public static void main(String[] args) {

        for (int i = 1; i <=10; i++){
            System.out.println("hello "+i);
        }
        for (int j=10; j>0;j--){
            System.out.println("yoo "+j);
        }

        String[] names = {"gota","namal","maina"};
        for (int i = 0; i < names.length; i++){
            System.out.println(names[i]);
        }

    }
}
